function FootSelectToggle(btn){
      var $this = $(btn);
      var txt = '하위메뉴';
      var $parent = $this.parent();
      if($parent.hasClass('active')){
          $parent.removeClass('active');
          $this.attr('title', txt + ' 닫힘');
      }else{
          $parent.addClass('active');
          $this.attr('title', txt + ' 열림');
      }
}

